import { Section } from '@renderer/pages/info/Section'

export function Caveats() {
  return (
    <Section id="caveats" title="9. Известные ограничения">
      <ul>
        <li>
          <b>Эндогенность.</b> Установка нового скилла может ↓ tokens не потому что агент стал
          эффективнее, а потому что задачи стали проще, или агент стал предпочитать короткие
          решения. Eff не различает «стал умнее» и «стал отговариваться». Бенчмарк-сьют — отдельная
          exogenous-проверка (раздел в разработке).
        </li>
        <li>
          <b>Замороженный бейзлайн стареет.</b> Atlas не обновляет бейзлайн автоматически. Если
          задачи за полгода систематически выросли по сложности, Eff будет дрейфовать вверх не из-за
          эффективности, а из-за выхода за пределы обучающего распределения. Рекомендация:
          rebaseline раз в N месяцев или при крупных изменениях характера работы.
        </li>
        <li>
          <b>Сезонность.</b> День недели / время суток в модели не учитываются.
        </li>
        <li>
          <b>Small-sample tail.</b> Per-session floor отбрасывает половину сессий из Eff-графика. Не
          баг: эти сессии слишком короткие, чтобы дать осмысленный ratio.
        </li>
        <li>
          <b>Difficulty.</b> Поле существует, но в формуле сейчас не участвует. Историческая причина
          — низкое покрытие.
        </li>
      </ul>
    </Section>
  )
}
